    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String id=jTextField1.getText();
        try
        {
           Connection con=ConnectionProvider.getCon();
            java.sql.Statement st =con.createStatement();
            ResultSet rs=st.executeQuery("select * from Reservation where id='"+id+"'");
            if(rs.next())
            {
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(rs.getString(7));
                jTextField6.setText(rs.getString(8));
                jTextField7.setText(rs.getString(6));
                jTextField8.setText(rs.getString(9));
                

            }else
            {
                JOptionPane.showMessageDialog(null, "ID Réservation n'existe pas");
            }

        }
        catch(Exception e)
        {

        }
    }//GEN-LAST:event_jButton3ActionPerformed

