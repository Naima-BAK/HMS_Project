    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        JFXPanel j=new JFXPanel();
        String uri1=new File("click.mp3.mp3").toURI().toString();
        new MediaPlayer(new Media(uri1)).play();
        String id=jTextField1.getText();
        String name=jTextField2.getText();
        String mobileNumber=jTextField3.getText();
        String checkIn=jTextField4.getText();
        String bed= jTextField5.getText();
        String roomType= jTextField6.getText();
        String roomNum= jTextField7.getText();
        String price=jTextField8.getText();

        try {

            Connection con = ConnectionProvider.getCon();
            Statement stmt = con.createStatement();
            stmt.execute("update Reservation set name='"+name+"',mobileNumber='"+mobileNumber+"',checkIn='"+checkIn+"',bed='"+bed+"',roomType='"+roomType+"',roomNum='"+roomNum+"',pricePerDay='"+price+"' where id='"+id+"'");
            JOptionPane.showMessageDialog(null, "Modifier avec succés");

        } catch (Exception  se) {
            JOptionPane.showMessageDialog(null, se);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

