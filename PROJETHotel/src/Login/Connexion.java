    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int check=0;
        String email=jTextField1.getText();
        String password=jPasswordField1.getText();
        if(jComboBox1.getSelectedItem()=="Administrateur"){
            if(email.equals("") || password.equals("")){
                JFXPanel j=new JFXPanel();
                String uri1=new File("erro.mp3.mp3").toURI().toString();
                new MediaPlayer(new Media(uri1)).play();

                check=1;
                JOptionPane.showMessageDialog(null, "Chaque champ est obligatoire");
            }
            else if(email.equals("hms") || password.equals("admin")){

                 JFXPanel j=new JFXPanel();
                 String uri=new File("click.mp3.mp3").toURI().toString();
                 new MediaPlayer(new Media(uri)).play();
                check=1;
                setVisible(false);
                new AccueilAdmin().setVisible(true);
            }
        }else if(jComboBox1.getSelectedItem()=="RÃ©ceptionniste")
        {
            ResultSet rs = Select.getData("select * from user where email='"+email+"' and password='"+password+"'");
            try{
                if(rs.next())
                {
                     JFXPanel j=new JFXPanel();
                     String uri=new File("click.mp3.mp3").toURI().toString();
                     new MediaPlayer(new Media(uri)).play();
                    check=1;
                    if(rs.getString(6).equals("true")){
                        setVisible(false);
                        new Accueil().setVisible(true);
                    }else{
                         
                        String ur2=new File("click.mp3.mp3").toURI().toString();
                         new MediaPlayer(new Media(ur2)).play();
                        JOptionPane.showMessageDialog(null,"attendre l'acceptation de l'administrateur");
                    }
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }

        if(check==0){
            JFXPanel j=new JFXPanel();
            String uri=new File("erro.mp3.mp3").toURI().toString();
            new MediaPlayer(new Media(uri)).play();
            JOptionPane.showMessageDialog(null, "email ou mot de passe est incorrect");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

